#include <iostream>
using namespace std;


int main(int argc, char const *argv[])
{
	int value = 65;  // 'A' 의 ASCII 값은 65 임 
	cout << value << endl;

	// value에 저장된 값을 변수 ch를 선언하고 value의 값을 ch에 복사하는 코드를 작성
	char ch = value;
	cout << ch << endl;

	cout << 1000 << endl;

	cout << 10.4567 << endl;

	cout << true << endl;

	cout << false << endl;
	return 0;
}